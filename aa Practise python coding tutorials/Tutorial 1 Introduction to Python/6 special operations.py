#Inter Devision (//)
result = 17 // 5    # Divides and rounds down
print(result)
#output: 3


#Modulus (%)
reminders = 17 % 5
print(reminders)     # Shows remainder after division
#output: 2


# Exponents (**)
square = 5 ** 2     # 5 squared = 5^2
cube = 2 ** 3       # 2 cubed   = 2^3
print(square)       # Shows: 25
print(cube)         # Shows: 8